use std::str::FromStr;

pub(crate) fn run() {
    let _input = "##.#.#
...##.
#....#
..#...
#.#..#
####.#";
    let _input = _get_input();

    let mut yard: Yard = _input.parse().unwrap();

    for i in 0..=100 {
        if i != 0 {
            yard.next();
        }
        println!("After {} steps:", i);
        println!("{}", yard.format());
        println!("{} on", yard.number_on());
        println!();
    }
}

struct Yard(Vec<Vec<Panel>>);

impl Yard {
    pub(crate) fn number_on(&self) -> usize {
        self.0.iter().map(|row| row.iter().filter(|cell| cell.state).count()).sum()
    }
    pub(crate) fn format(&self) -> String {
        format!("{}", self.0.iter().map(|row| row.iter().map(|cell| if cell.state { '#' } else { '.' }).collect::<String>()).collect::<Vec<String>>().join("\n"))
    }
    fn get_neighbors<'a>(&'a self, x: usize, y: usize) -> impl Iterator<Item=(usize, usize)> + 'a {
        (y.max(1) - 1..=y.min(self.0.len() - 2) + 1)
            .flat_map(move |y| (x.max(1) - 1..=x.min(self.0[y].len() - 2) + 1).map(move |x| (x, y)))
            .filter(move |(x0, y0)| *x0 != x || *y0 != y)
    }
    pub(crate) fn next(&mut self) {
        let mut old = vec![vec![Panel::default(); self.0[0].len()]; self.0.len()];
        std::mem::swap(&mut self.0, &mut old);
        let old = old;

        for y in 0..old.len() {
            for x in 0..old[y].len() {
                let is_corner = (x == 0 || x == old[y].len() - 1) && (y == 0 || y == old.len() - 1);
                self.0[y][x].state =
                    if is_corner {
                        true
                    } else {
                        let count = self.get_neighbors(x, y).filter(|(x, y)| old[*y][*x].state).count();
                        if old[y][x].state {
                            count == 2 || count == 3
                        } else {
                            count == 3
                        }
                    };
            }
        }
    }
}

impl FromStr for Yard {
    type Err = ();

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        Ok(Yard(s.split('\n').map(|line| {
            line.chars().map(|c| Panel {
                state: c == '#'
            }).collect::<Vec<Panel>>()
        }).collect()))
    }
}

